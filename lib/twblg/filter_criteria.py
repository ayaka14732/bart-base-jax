def is_special_token(c):
    return c in [
        '[PAD]',
        '[UNK]',
        '[CLS]',
        '[SEP]',
        '[MASK]',
    ]

def should_remove(c):
    return not is_special_token(c) and (len(c) > 1 or certain_trad(c) or is_symbol(c)) and c not in should_add
